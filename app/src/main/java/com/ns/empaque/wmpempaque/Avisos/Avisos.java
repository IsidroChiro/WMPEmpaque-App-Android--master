public class Avisos {

    private String titleSpanish, titleEnglish, descriptionSpanish, descriptionEnglish, fechaCreación, userCreacion;

    public String getDescriptionEnglish() {
        return descriptionEnglish;
    }

    public void setDescriptionEnglish(String descriptionEnglish) {
        this.descriptionEnglish = descriptionEnglish;
    }

    public String getDescriptionSpanish() {
        return descriptionSpanish;
    }

    public void setDescriptionSpanish(String descriptionSpanish) {
        this.descriptionSpanish = descriptionSpanish;
    }

    public String getFechaCreación() {
        return fechaCreación;
    }

    public void setFechaCreación(String fechaCreación) {
        this.fechaCreación = fechaCreación;
    }

    public String getTitleEnglish() {
        return titleEnglish;
    }

    public void setTitleEnglish(String titleEnglish) {
        this.titleEnglish = titleEnglish;
    }

    public String getTitleSpanish() {
        return titleSpanish;
    }

    public void setTitleSpanish(String titleSpanish) {
        this.titleSpanish = titleSpanish;
    }

    public String getUserCreacion() {
        return userCreacion;
    }

    public void setUserCreacion(String userCreacion) {
        this.userCreacion = userCreacion;
    }
}

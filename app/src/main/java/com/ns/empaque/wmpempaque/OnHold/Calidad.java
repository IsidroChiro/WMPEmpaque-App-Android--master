public class Calidad {

    private int idCalidad;
    private String nombreCalidad;
    private String nombreCortoCalidad;
    private String descripcionCalidad;
    private int factorCalidad;
    private int estadoCalidad;

    public void Calidad(){}

    public int getIdCalidad() {
        return idCalidad;
    }

    public void setIdCalidad(int idCalidad) {
        this.idCalidad = idCalidad;
    }

    public String getNombreCalidad() {
        return nombreCalidad;
    }

    public void setNombreCalidad(String nombreCalidad) {
        this.nombreCalidad = nombreCalidad;
    }

    public String getNombreCortoCalidad() {
        return nombreCortoCalidad;
    }

    public void setNombreCortoCalidad(String nombreCortoCalidad) {
        this.nombreCortoCalidad = nombreCortoCalidad;
    }

    public String getDescripcionCalidad() {
        return descripcionCalidad;
    }

    public void setDescripcionCalidad(String descripcionCalidad) {
        this.descripcionCalidad = descripcionCalidad;
    }

    public int getFactorCalidad() {
        return factorCalidad;
    }

    public void setFactorCalidad(int factorCalidad) {
        this.factorCalidad = factorCalidad;
    }

    public int getEstadoCalidad() {
        return estadoCalidad;
    }

    public void setEstadoCalidad(int estadoCalidad) {
        this.estadoCalidad = estadoCalidad;
    }
}
